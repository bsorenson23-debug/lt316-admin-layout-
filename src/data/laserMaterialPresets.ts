export interface PresetMatch {
  preset: LaserPreset;
  score: number; // 0–100
}

export function findLaserPresets(
  laserType: LaserSourceType,
  wattagePeak: number,
  materialSlug?: string,
  productHint?: string
): PresetMatch[] {
  return LASER_MATERIAL_PRESETS
    .filter((p) => p.laserTypes.includes(laserType))
    .map((p) => {
      let score = 0;
      // Wattage match: 40 pts max
      if (wattagePeak >= p.wattageMin && wattagePeak <= p.wattageMax) {
        score += 40;
      } else {
        const gap = Math.min(
          Math.abs(wattagePeak - p.wattageMin),
          Math.abs(wattagePeak - p.wattageMax)
        );
        score += Math.max(0, 40 - gap * 0.8);
      }
      // Material match: 40 pts
      if (materialSlug && p.material === materialSlug) score += 40;
      else if (materialSlug && p.material.includes(materialSlug)) score += 20;
      // Product hint: 20 pts
      if (productHint && p.productHints?.includes(productHint)) score += 20;
      // Confidence boost: up to 5 pts
      if (p.confidence === "verified") score += 5;
      else if (p.confidence === "community") score += 2;
      return { preset: p, score };
    })
    .filter((m) => m.score > 0)
    .sort((a, b) => b.score - a.score);
}

export function getBestPreset(
  laserType: LaserSourceType,
  wattagePeak: number,
  materialSlug?: string,
  productHint?: string
): LaserPreset | null {
  const matches = findLaserPresets(laserType, wattagePeak, materialSlug, productHint);
  return matches[0]?.preset ?? null;
}
