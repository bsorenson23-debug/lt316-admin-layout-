// 120x120 placeholder thumbnails as base64 data URLs.
// Colors: tumblers=#1e2a3a, mugs=#2a1e1e, bottles=#1a2a1a, flat=#1e1e2a
function placeholder(color: string): string {
  // Minimal 120x120 single-color PNG encoded as base64.
  // Generated at build time — these are static strings.
  // We use a canvas-free approach: a 1x1 BMP scaled via CSS,
  // but since we need valid data URLs, we embed tiny PNGs.
  // For seed data we use a runtime-generated approach.
  return `data:image/svg+xml,${encodeURIComponent(
    `<svg xmlns="http://www.w3.org/2000/svg" width="120" height="120">` +
      `<rect width="120" height="120" fill="${color}"/>` +
      `<rect x="4" y="4" width="112" height="112" rx="6" fill="none" stroke="${color === '#1e2a3a' ? '#2a3a4a' : color === '#2a1e1e' ? '#3a2a2a' : color === '#1a2a1a' ? '#2a3a2a' : '#2a2a3a'}" stroke-width="1"/>` +
      `</svg>`
  )}`;
}

